<?php

namespace ORMBundle\Entity;

/**
 * Store
 */
class Store
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $nom;

    /**
     * @var string
     */
    private $login;

    /**
     * @var string
     */
    private $password;

    /**
     * @var string
     */
    private $prix;

    /**
     * @var string
     */
    private $commentaire;

    /**
     * @var string
     */
    private $url;

    /**
     * @var string
     */
    private $mail;

    /**
     * @var \DateTime
     */
    private $nextUpdateDomain;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nom
     *
     * @param string $nom
     *
     * @return Store
     */
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get nom
     *
     * @return string
     */
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * Set login
     *
     * @param string $login
     *
     * @return Store
     */
    public function setLogin($login)
    {
        $this->login = $login;

        return $this;
    }

    /**
     * Get login
     *
     * @return string
     */
    public function getLogin()
    {
        return $this->login;
    }

    /**
     * Set password
     *
     * @param string $password
     *
     * @return Store
     */
    public function setPassword($password)
    {
        $this->password = $password;

        return $this;
    }

    /**
     * Get password
     *
     * @return string
     */
    public function getPassword()
    {
        return $this->password;
    }

    /**
     * Set prix
     *
     * @param string $prix
     *
     * @return Store
     */
    public function setPrix($prix)
    {
        $this->prix = $prix;

        return $this;
    }

    /**
     * Get prix
     *
     * @return string
     */
    public function getPrix()
    {
        return $this->prix;
    }

    /**
     * Set commentaire
     *
     * @param string $commentaire
     *
     * @return Store
     */
    public function setCommentaire($commentaire)
    {
        $this->commentaire = $commentaire;

        return $this;
    }

    /**
     * Get commentaire
     *
     * @return string
     */
    public function getCommentaire()
    {
        return $this->commentaire;
    }

    /**
     * Set url
     *
     * @param string $url
     *
     * @return Store
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }

    /**
     * Get url
     *
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * Set mail
     *
     * @param string $mail
     *
     * @return Store
     */
    public function setMail($mail)
    {
        $this->mail = $mail;

        return $this;
    }

    /**
     * Get mail
     *
     * @return string
     */
    public function getMail()
    {
        return $this->mail;
    }

    /**
     * Set nextUpdateDomain
     *
     * @param \DateTime $nextUpdateDomain
     *
     * @return Store
     */
    public function setNextUpdateDomain($nextUpdateDomain)
    {
        $this->nextUpdateDomain = $nextUpdateDomain;

        return $this;
    }

    /**
     * Get nextUpdateDomain
     *
     * @return \DateTime
     */
    public function getNextUpdateDomain()
    {
        return $this->nextUpdateDomain;
    }
}
